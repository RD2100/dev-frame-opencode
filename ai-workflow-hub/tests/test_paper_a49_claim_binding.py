class TestA49NonStrictWarning:
    """Non-strict completeness warning flag."""

    def test_non_strict_warning_flag(self, tmp_path):
        """completeness_strict=False and missing files -> completeness_warning=True."""
        files = {"data.txt": "base data"}
        zip_path = _make_bundle_zip(tmp_path, files)

        # Extra orphan file in run_dir -> completeness fails
        run_files = dict(files)
        run_files["extra-orphan.dat"] = "orphan data"
        rd = _make_run_dir(tmp_path, run_files)

        # Policy with completeness_strict=False (non-strict)
        policy = _valid_policy(completeness_strict=False)
        policy_path = _make_policy(tmp_path, policy)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--policy", policy_path,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        assert data["completeness_warning"] is True

    def test_strict_no_warning_flag(self, tmp_path):
        """completeness_strict=True and missing files -> no warning (it blocks instead)."""
        files = {"data.txt": "base data"}
        zip_path = _make_bundle_zip(tmp_path, files)

        # Extra orphan file in run_dir -> completeness fails
        run_files = dict(files)
        run_files["extra-orphan.dat"] = "orphan data"
        rd = _make_run_dir(tmp_path, run_files)

        # Policy with completeness_strict=True (strict)
        policy = _valid_policy(completeness_strict=True)
        policy_path = _make_policy(tmp_path, policy)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--policy", policy_path,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        # In strict mode, there is no warning flag -- it blocks instead
        assert "completeness_warning" not in data


