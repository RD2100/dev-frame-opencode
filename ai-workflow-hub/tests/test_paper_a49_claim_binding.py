class TestA49ClaimBinding:
    """Completeness claim binding: stored vs recomputed comparison."""

    def test_verified_matched(self, tmp_path):
        """Stored completeness matches recomputed -> trust_status is 'verified_matched'."""
        files = {"data.txt": "claim binding data"}
        # Stored completeness says complete=True, missing_count=0
        stored = {"complete": True, "missing_count": 0}
        zip_path = _make_bundle_zip(tmp_path, files, completeness=stored)

        # Run directory matches bundle exactly -> recomputed complete=True, missing_count=0
        rd = _make_run_dir(tmp_path, files)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        assert data["completeness_trust_status"] == "verified_matched"

    def test_verified_drift(self, tmp_path):
        """Stored completeness differs from recomputed -> trust_status is 'verified_drift'."""
        files = {"data.txt": "drift data"}
        # Stored completeness says complete=True, missing_count=0
        stored = {"complete": True, "missing_count": 0}
        zip_path = _make_bundle_zip(tmp_path, files, completeness=stored)

        # Run directory has an extra orphan file -> recomputed complete=False, missing_count=1
        run_files = dict(files)
        run_files["extra-orphan.dat"] = "orphan data not in bundle"
        rd = _make_run_dir(tmp_path, run_files)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        assert data["completeness_trust_status"] == "verified_drift"

    def test_verified_no_claim(self, tmp_path):
        """No stored completeness in attestation -> trust_status is 'verified_no_claim'."""
        files = {"data.txt": "no claim data"}
        # No stored completeness in attestation
        zip_path = _make_bundle_zip(tmp_path, files, completeness=None)

        # Run directory matches bundle exactly
        rd = _make_run_dir(tmp_path, files)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        assert data["completeness_trust_status"] == "verified_no_claim"


