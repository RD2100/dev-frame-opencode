"""A49 -- Completeness Claim Binding.

Verifies:
1. Completeness claim binding in paper verify: when --completeness-check is
   used with --run-dir and the attestation has a stored completeness report,
   the verifier compares recomputed vs stored and sets
   completeness_trust_status accordingly.
2. Non-strict completeness warning: when completeness_strict=False and
   completeness fails in verified mode, result["completeness_warning"]=True.
3. Claim-only entries in verify-chain: entries without run_dir now have
   verified=False and claim_only=True (A49 fix from A46's verified=True).
"""

