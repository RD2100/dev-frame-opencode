class TestA51ImmutableRaw:
    """Immutable raw results: raw_verdict reflects actual check outcomes
    and result['failed'] is never decremented for policy adjustment."""

    def test_raw_verdict_present(self, tmp_path):
        """raw_verdict exists in JSON output from paper verify."""
        files = {"data.txt": "a51 present data"}
        stored = {
            "complete": True,
            "missing_count": 0,
            "total_run_files": 1,
            "total_ignored": 0,
        }
        zip_path = _make_bundle_zip(tmp_path, files, completeness=stored)
        rd = _make_run_dir(tmp_path, files)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        assert "raw_verdict" in data
        assert data["raw_verdict"] in ("passed", "failed")

    def test_raw_verdict_reflects_failures(self, tmp_path):
        """When completeness fails, raw_verdict is 'failed' even if
        policy_verdict is 'passed'."""
        files = {"data.txt": "a51 raw fail data"}
        zip_path = _make_bundle_zip(tmp_path, files)

        # Extra orphan file -> completeness fails
        # Use persisted run_dir so check 10 passes (only completeness failure)
        rd = _make_run_dir_with_persisted(
            tmp_path, files, zip_path,
            extra_files={"extra-orphan.dat": "orphan data not in bundle"},
        )

        policy = _valid_policy(
            completeness_strict=False,
            ignored_artifacts=["bundle_manifest_*.json"],
        )
        policy_path = _make_policy(tmp_path, policy)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--policy", policy_path,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        # raw_verdict reflects actual failures (completeness failure present)
        assert data["raw_verdict"] == "failed"
        # policy_verdict is adjusted: warn doesn't count as failure
        assert data["policy_verdict"] == "passed"

    def test_no_counter_mutation(self, tmp_path):
        """result['failed'] is NOT decremented for non-strict completeness.

        A51 removed the old ``result["failed"] -= 1`` counter mutation.
        Proof: raw_verdict stays 'failed' (meaning failed > 0) while
        policy_verdict is 'passed' (meaning _policy_failed adjusted to 0).
        """
        files = {"data.txt": "a51 no mutation data"}
        zip_path = _make_bundle_zip(tmp_path, files)

        rd = _make_run_dir_with_persisted(
            tmp_path, files, zip_path,
            extra_files={"extra-orphan.dat": "orphan data not in bundle"},
        )

        policy = _valid_policy(
            completeness_strict=False,
            ignored_artifacts=["bundle_manifest_*.json"],
        )
        policy_path = _make_policy(tmp_path, policy)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--policy", policy_path,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        # If counter were mutated, raw_verdict would be "passed"
        # Since A51 keeps raw immutable, raw_verdict stays "failed"
        assert data["raw_verdict"] == "failed"
        assert data["policy_verdict"] == "passed"
        # The divergence proves no counter mutation occurred


