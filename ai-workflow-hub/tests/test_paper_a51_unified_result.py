class TestA51MissingHashComparison:
    """Missing-file hash comparison: stored vs recomputed path_hash sets."""

    def test_missing_hashes_match(self, tmp_path):
        """Stored and recomputed missing hashes match ->
        completeness_missing_hashes_match=True."""
        files = {"data.txt": "a51 hash match data"}

        # Pre-compute the path_hash for the orphan file that will be missing
        orphan_name = "extra-orphan.dat"
        orphan_hash = hashlib.sha256(orphan_name.encode("utf-8")).hexdigest()[:16]

        # Stored completeness with matching missing_from_bundle
        # total_run_files=2 (data.txt + extra-orphan.dat), total_ignored=1
        # (persisted bundle_manifest_*.json is ignored by policy)
        stored = {
            "complete": False,
            "missing_count": 1,
            "total_run_files": 2,
            "total_ignored": 1,
            "missing_from_bundle": [
                {"path_hash": orphan_hash, "basename": orphan_name}
            ],
        }
        zip_path = _make_bundle_zip(tmp_path, files, completeness=stored)

        rd = _make_run_dir_with_persisted(
            tmp_path, files, zip_path,
            extra_files={orphan_name: "orphan data"},
        )

        policy = _valid_policy(
            completeness_strict=False,
            ignored_artifacts=["bundle_manifest_*.json"],
        )
        policy_path = _make_policy(tmp_path, policy)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--policy", policy_path,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        assert data["completeness_missing_hashes_match"] is True
        # No drift upgrade since hashes match
        assert data["completeness_drift_severity"] == "none"

    def test_missing_hashes_differ(self, tmp_path):
        """Stored has different missing files ->
        completeness_missing_hashes_match=False, drift_severity upgraded."""
        files = {"data.txt": "a51 hash differ data"}

        # Stored completeness with a WRONG hash (different from recomputed)
        # Dimensions match (total_run_files=2, total_ignored=1, missing_count=1,
        # complete=False) so drift_dims=0; only the hash mismatch triggers upgrade
        stored = {
            "complete": False,
            "missing_count": 1,
            "total_run_files": 2,
            "total_ignored": 1,
            "missing_from_bundle": [
                {"path_hash": "0000000000000000", "basename": "wrong.txt"}
            ],
        }
        zip_path = _make_bundle_zip(tmp_path, files, completeness=stored)

        rd = _make_run_dir_with_persisted(
            tmp_path, files, zip_path,
            extra_files={"extra-orphan.dat": "orphan data"},
        )

        policy = _valid_policy(
            completeness_strict=False,
            ignored_artifacts=["bundle_manifest_*.json"],
        )
        policy_path = _make_policy(tmp_path, policy)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--policy", policy_path,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        assert data["completeness_missing_hashes_match"] is False
        # Drift upgraded from "none" to "low" due to hash mismatch
        assert data["completeness_drift_severity"] == "low"


