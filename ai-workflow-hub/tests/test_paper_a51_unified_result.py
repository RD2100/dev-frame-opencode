"""A51 -- Unified Raw-vs-Policy Result Model.

Verifies:
1. Immutable raw results: result["failed"] is never decremented (no counter
   mutation).  Raw check results stay unchanged regardless of policy adjustment.
2. raw_verdict field: "passed" if result["failed"] == 0, else "failed".
   Always reflects the actual check outcome without policy adjustment.
3. policy_verdict field: computed from raw_verdict but completeness warnings
   (completeness_policy_action="warn") do not count as failures.
4. Missing-file hash comparison: stored missing_from_bundle path_hash sets
   are compared against recomputed; mismatches upgrade drift to "low".
5. completeness_policy_severity: maps trust_status to policy severity
   ("none", "info", "warning", "block").
"""

