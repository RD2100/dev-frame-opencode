class TestA47BackwardCompat:
    """Backward compatibility: verify works without --completeness-check."""

    def test_verify_without_completeness_check(self, tmp_path):
        """Without --completeness-check, no completeness key in result."""
        zip_path = _make_bundle_zip(tmp_path, {"data.txt": "basic data"})
        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--json",
        ])
        data = json.loads(result.stdout, strict=False)
        assert "completeness" not in data

    def test_existing_verify_checks_still_pass(self, tmp_path):
        """Standard checks still pass on a well-formed ZIP with run_dir."""
        files = {"data.txt": "well-formed bundle"}
        zip_path = _make_bundle_zip(tmp_path, files)

        rd = _make_run_dir_with_persisted(tmp_path, files, zip_path)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--json",
        ])

        assert result.exit_code == 0, (
            f"Expected exit_code 0 but got {result.exit_code}. "
            f"stderr: {result.stderr}"
        )
        data = json.loads(result.stdout, strict=False)
        assert data["verdict"] == "passed"
        assert data["failed"] == 0
