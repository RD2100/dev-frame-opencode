"""A47 -- Verifier-Side Completeness Re-verification.

Verifies:
1. --completeness-check option exists on paper verify command
2. With --run-dir: re-computes completeness by comparing run directory files
   against the bundle manifest (bm_data["files"])
3. Without --run-dir: falls back to claim-only mode, checking stored
   completeness data in the attestation
4. Policy-controlled classification: ignored_artifacts (glob) and
   generated_artifacts (exact match)
5. Hash-redacted missing file reporting (path_hash + basename)
6. completeness report in result JSON
7. Backward compatibility: verify works without --completeness-check
"""

