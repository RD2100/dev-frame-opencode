class TestA47ClaimOnly:
    """Claim-only mode: --completeness-check without --run-dir."""

    def test_claim_only_without_run_dir(self, tmp_path):
        """Without --run-dir, completeness mode is claim_only."""
        stored = {"complete": True, "missing_count": 0}
        zip_path = _make_bundle_zip(
            tmp_path, {"data.txt": "claim data"},
            completeness=stored,
        )
        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--json",
            "--completeness-check",
        ])
        data = json.loads(result.stdout, strict=False)
        assert "completeness" in data
        assert data["completeness"]["mode"] == "claim_only"

    def test_claim_only_with_stored_completeness(self, tmp_path):
        """Stored completeness data in attestation is found and reported."""
        stored = {"complete": True, "missing_count": 0, "total_run_files": 5}
        zip_path = _make_bundle_zip(
            tmp_path, {"data.txt": "stored data"},
            completeness=stored,
        )
        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--json",
            "--completeness-check",
        ])
        data = json.loads(result.stdout, strict=False)
        comp = data["completeness"]
        assert comp["mode"] == "claim_only"
        assert "stored_completeness" in comp
        assert comp["stored_completeness"]["complete"] is True

    def test_claim_only_without_stored_completeness(self, tmp_path):
        """No stored completeness in attestation: claim check fails."""
        zip_path = _make_bundle_zip(
            tmp_path, {"data.txt": "no completeness here"},
            completeness=None,
        )
        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--json",
            "--completeness-check",
        ])
        data = json.loads(result.stdout, strict=False)
        comp = data["completeness"]
        assert comp["mode"] == "claim_only"
        assert comp["verified"] is False


