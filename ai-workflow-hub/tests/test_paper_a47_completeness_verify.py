class TestA47FullReverification:
    """Full re-verification: --completeness-check --run-dir."""

    def test_verified_complete(self, tmp_path):
        """All run_dir files match bundle manifest: complete=True, mode=verified."""
        files = {"data.txt": "complete data"}
        zip_path = _make_bundle_zip(tmp_path, files)

        # Run directory contains exactly the user files listed in the
        # bundle manifest.  We use _make_run_dir (not the persisted
        # variant) because the persisted manifest file would be an extra
        # file in the run_dir not listed in the bundle manifest.
        rd = _make_run_dir(tmp_path, files)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        assert "completeness" in data
        comp = data["completeness"]
        assert comp["mode"] == "verified"
        assert comp["complete"] is True
        assert comp["missing_count"] == 0

    def test_verified_missing_files(self, tmp_path):
        """Files in run_dir not in bundle manifest are detected as missing."""
        files = {"data.txt": "base data"}
        zip_path = _make_bundle_zip(tmp_path, files)

        # Extra orphan file in run_dir that is NOT in the bundle manifest
        run_files = dict(files)
        run_files["extra-orphan.dat"] = "orphan data not in bundle"
        rd = _make_run_dir(tmp_path, run_files)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        comp = data["completeness"]
        assert comp["mode"] == "verified"
        assert comp["complete"] is False
        assert comp["missing_count"] > 0
        assert len(comp["missing_from_bundle"]) > 0
        # Each missing entry is a dict with path_hash and basename
        for entry in comp["missing_from_bundle"]:
            assert isinstance(entry, dict)
            assert "path_hash" in entry
            assert "basename" in entry

    def test_verified_with_policy_ignored(self, tmp_path):
        """Files matching ignored_artifacts patterns are excluded from missing."""
        files = {"data.txt": "base data"}
        zip_path = _make_bundle_zip(tmp_path, files)

        # debug.log in run_dir would normally be reported as missing,
        # but ignored_artifacts=["*.log"] excludes it.
        run_files = dict(files)
        run_files["debug.log"] = "debug trace output"
        rd = _make_run_dir(tmp_path, run_files)

        policy = _valid_policy(ignored_artifacts=["*.log"])
        policy_path = _make_policy(tmp_path, policy)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--policy", policy_path,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        comp = data["completeness"]
        assert comp["mode"] == "verified"
        # .log file excluded by policy -> no missing files
        assert comp["complete"] is True
        assert comp["missing_count"] == 0
        assert comp["total_ignored"] >= 1


