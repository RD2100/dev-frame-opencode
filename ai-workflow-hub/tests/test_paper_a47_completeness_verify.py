class TestA47VerifyOption:
    """--completeness-check option on paper verify command."""

    def test_verify_has_completeness_check_option(self):
        """--completeness-check is a recognized option on paper verify."""
        result = runner.invoke(app, ["paper", "verify", "--help"])
        assert result.exit_code == 0
        combined = (result.stdout or "") + (result.stderr or "")
        # Typer may truncate long option names in help output; check for
        # the distinctive prefix that cannot appear from any other option.
        assert "--completeness-ch" in combined or "--completeness-check" in combined

    def test_verify_completeness_in_json(self, tmp_path):
        """paper verify --json --completeness-check produces completeness in JSON."""
        zip_path = _make_bundle_zip(tmp_path, {"data.txt": "hello a47"})
        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--json",
            "--completeness-check",
        ])
        # Parse JSON from stdout (progress messages go to stderr)
        data = json.loads(result.stdout, strict=False)
        assert "completeness" in data


