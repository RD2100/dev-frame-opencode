class TestA48DeferredVerdict:
    """Verdict and trust_summary are computed AFTER completeness check."""

    def test_verdict_reflects_completeness_failure(self, tmp_path):
        """When completeness fails (extra file in run_dir), verdict is 'failed'."""
        files = {"data.txt": "base data"}
        zip_path = _make_bundle_zip(tmp_path, files)

        # Extra orphan file in run_dir not in bundle manifest
        run_files = dict(files)
        run_files["extra-orphan.dat"] = "orphan data not in bundle"
        rd = _make_run_dir(tmp_path, run_files)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        # The completeness check found missing files -> check failed
        assert data["failed"] > 0
        # A48: Verdict is computed AFTER completeness block, so it reflects
        # the completeness failure
        assert data["verdict"] == "failed"

    def test_verdict_reflects_completeness_success(self, tmp_path):
        """When completeness passes (all files match), verdict is 'passed'."""
        files = {"data.txt": "complete data"}
        zip_path = _make_bundle_zip(tmp_path, files)

        # Use persisted run_dir so check 10 (persisted_manifest) passes.
        # The persisted manifest file is an extra file in the run_dir that
        # would normally be flagged as missing from the bundle.  We use a
        # policy with ignored_artifacts to exclude it from the completeness
        # scan so that only user files are compared.
        rd = _make_run_dir_with_persisted(tmp_path, files, zip_path)

        policy = _valid_policy(ignored_artifacts=["bundle_manifest_*.json"])
        policy_path = _make_policy(tmp_path, policy)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--policy", policy_path,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        assert data["verdict"] == "passed"
        assert data["completeness_verdict"] == "verified"
        assert data["completeness"]["complete"] is True

    def test_trust_summary_includes_completeness_suffix(self, tmp_path):
        """When completeness passes, trust_summary ends with '_complete'."""
        files = {"data.txt": "suffix check data"}
        zip_path = _make_bundle_zip(tmp_path, files)

        rd = _make_run_dir(tmp_path, files)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--json",
            "--completeness-check",
        ])

        data = json.loads(result.stdout, strict=False)
        assert data["trust_summary"].endswith("_complete")


