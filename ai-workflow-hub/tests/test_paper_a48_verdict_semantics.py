class TestA48BackwardCompat:
    """Backward compatibility: no completeness suffix without the flag."""

    def test_no_completeness_no_suffix(self, tmp_path):
        """Without --completeness-check, trust_summary has no completeness suffix."""
        zip_path = _make_bundle_zip(tmp_path, {"data.txt": "basic data"})

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--json",
        ])

        data = json.loads(result.stdout, strict=False)
        ts = data["trust_summary"]
        assert "_complete" not in ts
        assert "_incomplete" not in ts
        assert "_claim_only" not in ts

    def test_existing_checks_unaffected(self, tmp_path):
        """Standard verify checks still work identically."""
        files = {"data.txt": "well-formed bundle"}
        zip_path = _make_bundle_zip(tmp_path, files)

        rd = _make_run_dir_with_persisted(tmp_path, files, zip_path)

        result = runner.invoke(app, [
            "paper", "verify",
            "--zip", zip_path,
            "--run-dir", rd,
            "--json",
        ])

        assert result.exit_code == 0, (
            f"Expected exit_code 0 but got {result.exit_code}. "
            f"stderr: {result.stderr}"
        )
        data = json.loads(result.stdout, strict=False)
        assert data["verdict"] == "passed"
        assert data["failed"] == 0
