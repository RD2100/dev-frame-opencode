def _file_sha256(content: str) -> str:
    """Compute SHA-256 hex digest for string content."""
    return hashlib.sha256(content.encode("utf-8")).hexdigest()


def _make_bundle_manifest(files: dict[str, str], bundle_id: str = _BUNDLE_ID) -> dict:
    """Build a bundle_manifest.json dict from {path: content}.

    The bundle manifest ``files`` list only contains *user* files (the ones
    the audit command would have found in the run directory).  Metadata
    members (bundle_manifest.json, attestation.json, MANIFEST.json) are
    generated by the audit and are NOT in the files list.

    Computes per-file sha256 and the attestation content_hash so that
    the verify command's content_hash_valid check passes.
    """
    file_entries = []
    for path, content in files.items():
        file_entries.append({"path": path, "sha256": _file_sha256(content)})

    sorted_entries = sorted([(f["path"], f["sha256"]) for f in file_entries])
    content_hash = hashlib.sha256(
        json.dumps(sorted_entries, sort_keys=True).encode("utf-8")
    ).hexdigest()

    return {
        "bundle_id": bundle_id,
        "files": file_entries,
        "attestation": {"content_hash": content_hash},
    }


def _make_attestation(file_entries: list[dict], completeness=None) -> dict:
    """Build an attestation.json dict.

    artifact_hashes mirrors the non-generated file entries from the bundle
    manifest so that the verify command's attestation_consistency check passes.

    _AUDIT_GENERATED_MEMBERS = {bundle_manifest.json, attestation.json,
                                MANIFEST.json, artifact_chain.json}
    """
    generated = {"bundle_manifest.json", "attestation.json",
                 "MANIFEST.json", "artifact_chain.json"}
    artifact_hashes = [
        {"artifact": f["path"], "sha256": f["sha256"]}
        for f in file_entries
        if f["path"] not in generated
    ]
    att: dict = {"artifact_hashes": artifact_hashes}
    if completeness is not None:
        att["completeness"] = completeness
    return att


def _make_bundle_zip(
    tmp_path: Path,
    files: dict[str, str],
    completeness=None,
    zip_name: str = "bundle.zip",
    bundle_id: str = _BUNDLE_ID,
) -> str:
    """Create a minimal valid audit bundle ZIP and matching sidecar.

    The ZIP contains:
    - User files from *files*
    - bundle_manifest.json (files list = user files only)
    - attestation.json (artifact_hashes = user files only)
    - MANIFEST.json (file index for all ZIP members)

    A matching .sha256 sidecar is written alongside the ZIP.

    Returns the ZIP path as a string.
    """
    bm = _make_bundle_manifest(files, bundle_id=bundle_id)

    # All ZIP members: user files + metadata
    all_files = dict(files)
    all_files["bundle_manifest.json"] = json.dumps(bm, indent=2)
    all_files["attestation.json"] = json.dumps(
        _make_attestation(bm["files"], completeness), indent=2
    )

    # MANIFEST.json -- file index with sha256 for every member
    manifest_entries = []
    for path in sorted(all_files.keys()):
        manifest_entries.append({
            "path": path,
            "sha256": _file_sha256(all_files[path]),
        })
    # Self-entry for MANIFEST.json (hash must be "" so verify skips it)
    manifest_entries.append({"path": "MANIFEST.json", "sha256": ""})
    manifest_json = json.dumps({"files": manifest_entries}, indent=2)
    all_files["MANIFEST.json"] = manifest_json

    # Write the ZIP
    zip_path = tmp_path / zip_name
    with zf_mod.ZipFile(zip_path, "w") as z:
        for path, content in all_files.items():
            z.writestr(path, content)

    # Write sidecar so sidecar_hash_match passes
    actual_hash = hashlib.sha256(zip_path.read_bytes()).hexdigest()
    sidecar_path = Path(str(zip_path) + ".sha256")
    sidecar_path.write_text(f"{actual_hash}  {zip_path.name}\n", encoding="utf-8")

    return str(zip_path)


def _make_run_dir(
    tmp_path: Path,
    files: dict[str, str],
    dir_name: str = "run",
) -> str:
    """Create a mock run directory with the given files.

    Returns the run directory path as a string.
    """
    rd = tmp_path / dir_name
    rd.mkdir(parents=True, exist_ok=True)
    for name, content in files.items():
        fpath = rd / name
        fpath.parent.mkdir(parents=True, exist_ok=True)
        fpath.write_text(content, encoding="utf-8")
    return str(rd)


def _make_run_dir_with_persisted(
    tmp_path: Path,
    user_files: dict[str, str],
    zip_path: str,
    bundle_id: str = _BUNDLE_ID,
    extra_files: dict[str, str] | None = None,
    dir_name: str = "run",
) -> str:
    """Create a run directory with user files and a persisted bundle manifest.

    The persisted manifest is written at
    ``<run_dir>/bundle_manifest_<bundle_id>.json`` with the correct
    ``zip_sha256`` so that check 10 (persisted_manifest_zip_sha256) passes.

    *extra_files* are additional files placed in the run directory (useful
    for simulating orphan files or policy-ignored artifacts).

    Returns the run directory path as a string.
    """
    rd = tmp_path / dir_name
    rd.mkdir(parents=True, exist_ok=True)

    # Write user files
    for name, content in user_files.items():
        fpath = rd / name
        fpath.parent.mkdir(parents=True, exist_ok=True)
        fpath.write_text(content, encoding="utf-8")

    # Write extra files
    if extra_files:
        for name, content in extra_files.items():
            fpath = rd / name
            fpath.parent.mkdir(parents=True, exist_ok=True)
            fpath.write_text(content, encoding="utf-8")

    # Write persisted bundle manifest
    zip_hash = hashlib.sha256(Path(zip_path).read_bytes()).hexdigest()
    persisted = {
        "bundle_id": bundle_id,
        "zip_sha256": zip_hash,
    }
    persisted_path = rd / f"bundle_manifest_{bundle_id}.json"
    persisted_path.write_text(json.dumps(persisted), encoding="utf-8")

    return str(rd)


def _make_policy(tmp_path: Path, policy_dict: dict, name: str = "policy.json") -> str:
    """Write a policy dict to a JSON file and return the path as string."""
    p = tmp_path / name
    p.write_text(json.dumps(policy_dict), encoding="utf-8")
    return str(p)


def _valid_policy(**overrides) -> dict:
    """Return a valid audit policy dict with optional overrides."""
    base = {
        "schema_version": "1.0",
        "signature_policy": "optional",
        "chain_verification_mode": "chain_only",
        "allowed_key_ids": [],
        "required_artifacts": [],
        "strict_chain": False,
        "strict_timestamps": True,
        "description": "test-policy-a48",
    }
    base.update(overrides)
    return base


